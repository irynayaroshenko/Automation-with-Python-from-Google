"""What is Git? Installing GIT"""
# Git has a distributed architecture: every person contributing to a repository has full copy of the repository on
# their own development machines.

# Git clients can communicate with Git servers over the network using HTTP,
# SSH or Git's own special protocol.

# You can use Git with or without a network connection.
# You can use it for small projects with like one developer or huge projects with thousands of contributors.
# You can use it to track private work that you can keep to yourself, or you can share your work with others by hosting
# a code on public servers like Github, Gitlab or others.

