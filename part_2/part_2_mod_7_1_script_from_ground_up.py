"""Writing a Script from the Ground Up"""
# Sterps for coding project:
# 1. Understand the problem statement
# This includes spelling out what needs to be done and identifying what the given inputs and desired outputs are
# for that program that we need to write.

# 2. Research
# This means figuring out how we can tackle the problem by the tools provided by the Python standard library or by
# external modules.
# No matter how tricky and intricate the challenge appears, chances are that others have solved something similar before
# So it's valuable that we spent some time looking into what resources exist to help us solve our problem.
# This research phase also includes looking at the documentation of the modules, classes, and functions that we'll
# need to use, and understanding how they should be applied.
# A lot of the documentation also includes examples.
# So it's helpful to absorb those and see how they relate to the code that we need to write.

# 3. Planning
# This means thinking about what data types are useful for our solution, order of operations that we need to perform,
# and how all the pieces have come together to form our solution. If the problem is complex,
# it might help to write down the plan for quick reference, either on a piece of paper or in a digital document.
# Writing down the plan helps us focus on how we're going to do things and identify any problems our plan might have.
# At many companies, it's a common practice to write a design document at this stage, detailing the problem statement,
# the tools that will be used to solve it, and the plan of attack towards a solution.
# Having others comment on your design helps make sure that all the twists have been untangled.

# 4. Writing
# This step includes not only writing the code, but also checking that the code does what it's supposed to do.
# We do that by both manually testing the code and adding some automatic test.